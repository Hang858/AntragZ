// --- Helper Functions ---

// Montgomery Reduction: a * R^-1 mod Q
static inline int32_t mont_reduce(int64_t a) {
    int32_t q = POLY_512_Q;
    int32_t inv_q = 150982657; // 12289^-1 mod 2^32
    int64_t t = (int64_t)((int32_t)a * inv_q);
    t = (a - t * q) >> 32;
    return (int32_t)(t < 0 ? t + q : t);
}

// Convert to Montgomery: val -> val * R mod Q
static inline int32_t to_mont(int32_t val) {
    return (int32_t)(((int64_t)val * MONT_R) % POLY_512_Q);
}

// --- 512-NTT Logic ---

static void ntt_512_forward(int32_t *out, const int16_t *in) {
    int32_t even[256], odd[256];
    
    // Split (Natural) & Convert to Mont
    for (int i = 0; i < 256; i++) {
        even[i] = to_mont(in[2*i]);
        odd[i]  = to_mont(in[2*i+1]);
    }

    // Hardware NTT (256)
    OP_ntt256(even, even, 0);
    OP_ntt256(odd, odd, 0);

    // Merge (Using BR Tables)
    for (int i = 0; i < 256; i++) {
        // odd[i] * twiddle
        int32_t t = mont_reduce((int64_t)odd[i] * PHI_512_FWD[i]);
        
        int32_t u = even[i];
        
        int32_t res_add = u + t;
        if (res_add >= POLY_512_Q) res_add -= POLY_512_Q;
        
        int32_t res_sub = u - t;
        if (res_sub < 0) res_sub += POLY_512_Q;
        
        out[i] = res_add;
        out[i+256] = res_sub;
    }
}

static void ntt_512_inverse(int32_t *out, const int32_t *in) {
    int32_t even[256], odd[256];
    
    // Split
    for (int i = 0; i < 256; i++) {
        int32_t u = in[i];
        int32_t v = in[i+256];
        
        int32_t sum = u + v;
        if (sum >= POLY_512_Q) sum -= POLY_512_Q;
        even[i] = sum;
        
        int32_t diff = u - v;
        if (diff < 0) diff += POLY_512_Q;
        
        // diff * inv_twiddle
        odd[i] = mont_reduce((int64_t)diff * PHI_512_INV[i]);
    }
    
    // Hardware INTT (256)
    // HW scales by inv_n passed in init
    OP_intt256(even, even, 1);
    OP_intt256(odd, odd, 1);
    
    // Merge
    for (int i = 0; i < 256; i++) {
        out[2*i]   = even[i];
        out[2*i+1] = odd[i];
    }
}

// --- Public Interface ---

void poly_ntt_512_init(void) {
    int32_t count = 2; 
    int32_t k = 0; 
    int32_t inv_q = 150982657; 
    
    // CRITICAL FIX: Use STANDARD DOMAIN inv_n for 1/512
    // 1/512 mod 12289 = 12265
    // The HW OP_intt256 performs MontMul(x, inv_n), effectively removing one R factor.
    // If we pass Mont(1/512), we end up with Mont(result).
    // Passing Std(1/512) gives Std(result).
    int32_t inv_n_512_std = INV_512_STD;

    OP_ntt256_init(OMEGA_HW_256, count, POLY_512_Q, inv_q, inv_n_512_std, k);
}

int poly_ntt_512_mul(int16_t *res, const int16_t *a, const int16_t *b) {
    int32_t buf_a[512];
    int32_t buf_b[512];
    int32_t buf_res[512];
    
    ntt_512_forward(buf_a, a);
    ntt_512_forward(buf_b, b);
    
    // CWM for 512 elements
    OP_cwm(buf_res, buf_a, buf_b, 256, POLY_512_Q, 0);
    OP_cwm(buf_res + 256, buf_a + 256, buf_b + 256, 256, POLY_512_Q, 0);
    
    ntt_512_inverse(buf_res, buf_res);
    
    // Output (Result is in Standard Domain)
    for (int i = 0; i < 512; i++) {
        int32_t val = buf_res[i];
        if (val >= POLY_512_Q) val %= POLY_512_Q;
        res[i] = (int16_t)val;
    }
    
    return 0;
}